package org.camunda.bpm;

public class Child {

    private String name = "Child Name";

    private Long age = 10L;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getAge() {
        return age;
    }

    public void setAge(Long age) {
        this.age = age;
    }
}
